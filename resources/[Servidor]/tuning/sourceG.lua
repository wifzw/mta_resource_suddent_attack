function fillPaintjobsTable(vehicle)
	local paintjobs = false 
	local menu = nil

	for k, cat in pairs(tunings) do
		if cat.name == "Paintjob" then
			menu = k
			break	
		end
	end
	
	tunings[menu].subMenu = {}
	tunings[menu].subMenu[0] = {
		name = "Stok",
		price = 0,
	}

	if paintjobs then
		if #paintjobs > 0 then
			for k, v in pairs(paintjobs) do
				table.insert(tunings[menu].subMenu, {
					name = v[3],
					price = 500,
					onlyFaction = v[4]
				})
			end
		end
	end
end

function rotateAround(angle, x, y, x2, y2)
	local radiant = math.rad(angle)
	local rotatedX = (x2 or 0) + x * math.cos(radiant) - y * math.sin(radiant)
	local rotatedY = (y2 or 0) + x * math.sin(radiant) + y * math.cos(radiant)
	return rotatedX, rotatedY
end

