function Arac()
    local txd = engineLoadTXD ('arac.txd')
    engineImportTXD(txd,411)
    local dff = engineLoadDFF('arac.dff')
    engineReplaceModel(dff,411)
end
addEventHandler('onClientResourceStart',getResourceRootElement(getThisResource()),Arac)


